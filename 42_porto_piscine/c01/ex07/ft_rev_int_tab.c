void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	b;
	int	swap;

	i = 0;
	b = size - 1;
	while (i < (size / 2))
	{
		swap = tab[i];
		tab[i] = tab[b];
		tab[b] = swap;
		i++;
		b--;
	}
}
