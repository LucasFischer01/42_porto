void	ft_sort_int_tab(int *tab, int size)
{
	int	c;
	int	swap;

	c = -1;
	while (++c < (size - 1))
	{
		if (tab[c] > tab[c + 1])
		{
			swap = tab[c];
			tab[c] = tab[c + 1];
			tab[c + 1] = swap;
			c = -1;
		}
	}
}
